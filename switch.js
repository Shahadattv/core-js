//  SWITCH EXACT USE
var color = "yellow";

switch (color) {
    case 'blue':
        console.log("colo is blue");
        break
    case 'red':
        console.log("colo is red");
        break
    case 'black':
        console.log("colo is black");
        break
    case "gray":
        console.log("colo is gray");
        break
    case "yellow":
        console.log("colo is yellow");
        break
    default:
        console.log(" You did't find exact coloe");
}